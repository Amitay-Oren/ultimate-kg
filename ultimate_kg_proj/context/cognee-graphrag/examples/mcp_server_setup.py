import os
import subprocess
import json
import time
from pathlib import Path
from typing import Optional, Dict, Any


def clone_cognee_if_needed() -> Optional[Path]:
    """Clone Cognee repository if not already present."""
    print("ğŸ“¦ Setting up Cognee MCP Server")
    
    # Check if already exists
    possible_locations = [
        Path.cwd() / "cognee",
        Path.home() / "cognee", 
        Path.cwd().parent / "cognee"
    ]
    
    for location in possible_locations:
        cognee_mcp_path = location / "cognee-mcp"
        if cognee_mcp_path.exists():
            print(f"âœ… Found existing Cognee at: {cognee_mcp_path}")
            return cognee_mcp_path
    
    # Clone repository
    print("ğŸ“¥ Cloning Cognee repository...")
    try:
        subprocess.run([
            "git", "clone", 
            "https://github.com/topoteretes/cognee.git"
        ], check=True)
        
        cognee_mcp_path = Path.cwd() / "cognee" / "cognee-mcp"
        if cognee_mcp_path.exists():
            print(f"âœ… Cloned Cognee to: {cognee_mcp_path}")
            return cognee_mcp_path
        else:
            print("âŒ Clone succeeded but cognee-mcp directory not found")
            return None
            
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed to clone Cognee: {e}")
        return None
    except FileNotFoundError:
        print("âŒ Git not found. Please install git or clone manually:")
        print("   git clone https://github.com/topoteretes/cognee.git")
        return None


def install_dependencies(cognee_mcp_path: Path) -> bool:
    """Install Cognee MCP server dependencies."""
    print("ğŸ“¦ Installing dependencies...")
    
    try:
        # Check if uv is available
        subprocess.run(["uv", "--version"], check=True, capture_output=True)
        
        # Install dependencies
        result = subprocess.run([
            "uv", "sync", "--dev", "--all-extras"
        ], cwd=cognee_mcp_path, check=True, capture_output=True, text=True)
        
        print("âœ… Dependencies installed successfully")
        return True
        
    except FileNotFoundError:
        print("âŒ UV not found. Please install UV:")
        print("   curl -LsSf https://astral.sh/uv/install.sh | sh")
        return False
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed to install dependencies: {e}")
        print(f"Error output: {e.stderr}")
        return False


def setup_neo4j() -> bool:
    """Setup Neo4j database using Docker."""
    print("ğŸ—„ï¸  Setting up Neo4j database...")
    
    try:
        # Check if Docker is available
        subprocess.run(["docker", "--version"], check=True, capture_output=True)
        
        # Check if Neo4j container already exists
        result = subprocess.run([
            "docker", "ps", "-a", "--filter", "name=neo4j-cognee"
        ], capture_output=True, text=True)
        
        if "neo4j-cognee" in result.stdout:
            print("ğŸ”„ Neo4j container already exists, starting...")
            subprocess.run(["docker", "start", "neo4j-cognee"], check=True)
        else:
            print("ğŸš€ Creating new Neo4j container...")
            subprocess.run([
                "docker", "run", "-d",
                "--name", "neo4j-cognee",
                "-p", "7474:7474",
                "-p", "7687:7687", 
                "-e", "NEO4J_AUTH=neo4j/password123",
                "neo4j:latest"
            ], check=True)
        
        # Wait for Neo4j to start
        print("â³ Waiting for Neo4j to start...")
        time.sleep(10)
        
        print("âœ… Neo4j is running at http://localhost:7474")
        print("   Username: neo4j")
        print("   Password: password123")
        return True
        
    except FileNotFoundError:
        print("âŒ Docker not found. Neo4j setup skipped.")
        print("ğŸ’¡ Install Docker or set up Neo4j manually")
        return False
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed to setup Neo4j: {e}")
        return False


def create_env_file(cognee_mcp_path: Path) -> bool:
    """Create .env file with database configuration."""
    print("âš™ï¸  Creating environment configuration...")
    
    env_file = cognee_mcp_path / ".env"
    
    # Get API key from user or environment
    api_key = os.getenv("OPENAI_API_KEY") or os.getenv("LLM_API_KEY")
    if not api_key:
        api_key = input("Enter your OpenAI API key: ").strip()
        if not api_key:
            print("âŒ API key required for MCP server")
            return False
    
    env_content = f"""# Cognee MCP Server Configuration
# Generated by setup script

# Required: LLM API Key  
LLM_API_KEY={api_key}

# Database Providers
VECTOR_DB_PROVIDER=lancedb
GRAPH_DATABASE_PROVIDER=neo4j
DB_PROVIDER=sqlite

# Neo4j Configuration
GRAPH_DATABASE_URL=bolt://localhost:7687
GRAPH_DATABASE_USERNAME=neo4j
GRAPH_DATABASE_PASSWORD=password123

# Optional: Embedding Configuration
EMBEDDING_PROVIDER=fastembed
EMBEDDING_MODEL=sentence-transformers/all-MiniLM-L6-v2
EMBEDDING_DIMENSIONS=384
EMBEDDING_MAX_TOKENS=256

# Environment
ENV=local
"""
    
    env_file.write_text(env_content)
    print(f"âœ… Created environment file: {env_file}")
    return True


def create_claude_settings(cognee_mcp_path: Path) -> Path:
    """Create Claude Code MCP settings file."""
    print("ğŸ¤– Creating Claude Code configuration...")
    
    settings = {
        "mcpServers": {
            "cognee": {
                "command": "uv",
                "args": ["run", "python", "src/server.py"],
                "cwd": str(cognee_mcp_path.resolve()),
                "env": {
                    "LLM_API_KEY": "${OPENAI_API_KEY}"
                }
            }
        }
    }
    
    settings_file = Path.cwd() / "claude_settings.json"
    settings_file.write_text(json.dumps(settings, indent=2))
    
    print(f"âœ… Created Claude Code settings: {settings_file}")
    return settings_file


def test_mcp_server(cognee_mcp_path: Path) -> bool:
    """Test MCP server startup."""
    print("ğŸ§ª Testing MCP server...")
    
    try:
        # Start server with timeout
        process = subprocess.Popen([
            "uv", "run", "python", "src/server.py"
        ], cwd=cognee_mcp_path, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
        
        # Wait briefly for startup
        try:
            stdout, stderr = process.communicate(timeout=8)
            if process.returncode == 0:
                print("âœ… MCP server test successful")
                return True
            else:
                print(f"âš ï¸  MCP server exited with code {process.returncode}")
                print(f"Error: {stderr}")
                return False
        except subprocess.TimeoutExpired:
            process.terminate()
            process.wait()
            print("âœ… MCP server started successfully (test terminated)")
            return True
            
    except Exception as e:
        print(f"âŒ MCP server test failed: {e}")
        return False


